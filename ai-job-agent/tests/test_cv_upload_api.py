def test_api_upload_duplicate_returns_409(db_path, cvs_dir, monkeypatch):
    _isolate_upload(monkeypatch, db_path, cvs_dir)
    user = register_test_user(email="dup@example.com", db_path=db_path)
    client = TestClient(api_server.app)
    payload = {"file": ("CV.pdf", b"%PDF-same-bytes", "application/pdf")}
    first = client.post("/cvs/upload", headers=auth_header_for(user), files=payload)
    assert first.status_code == 200, first.text
    second = client.post(
        "/cvs/upload",
        headers=auth_header_for(user),
        files={"file": ("CV-copy.pdf", b"%PDF-same-bytes", "application/pdf")},
    )
    assert second.status_code == 409


def test_create_cv_runs_full_init_db_without_autoincrement_when_pg(monkeypatch, tmp_path, cvs_dir):
    """Simulate Postgres mode: DDL must not use AUTOINCREMENT during create_cv."""
    executed: list[str] = []

    class _FakeConn:
        def execute(self, sql, params=None):
            executed.append(sql if isinstance(sql, str) else str(sql))
            return type("C", (), {"fetchall": lambda self: [], "fetchone": lambda self: None, "rowcount": 0})()

        def executescript(self, script):
            executed.append(script)

        def commit(self):
            pass

        def rollback(self):
            pass

    class _Ctx:
        def __enter__(self):
            return _FakeConn()

        def __exit__(self, *a):
            return False

    monkeypatch.setattr(db, "uses_postgres", lambda: True)
    monkeypatch.setattr(db, "get_connection", lambda *a, **k: _Ctx())
    monkeypatch.setattr(db, "REGISTRY_DB_PATH", tmp_path / "registry.db")
    monkeypatch.setattr(db, "ensure_auth_schema", lambda *a, **k: None)
    monkeypatch.setattr(db, "init_registry_db", lambda *a, **k: None)
    # Still need get_user etc. skipped — just call migration helper
    with db.get_connection(tmp_path / "x") as conn:
        db._apply_cv_match_migrations(conn)

    joined = "\n".join(executed).upper()
    assert "CV_TAILOR_VERSIONS" in joined
    assert "AUTOINCREMENT" not in joined
    assert "GENERATED BY DEFAULT AS IDENTITY" in joined
